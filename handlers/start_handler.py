    """‡¶≠‡ßá‡¶∞‡¶ø‡¶´‡¶ø‡¶ï‡ßá‡¶∂‡¶® ‡¶∏‡¶´‡¶≤ ‡¶π‡¶≤‡ßá ‡¶ì‡¶Ø‡¶º‡ßá‡¶≤‡¶ï‡¶æ‡¶Æ ‡¶Æ‡ßá‡¶∏‡ßá‡¶ú ‡¶è‡¶¨‡¶Ç ‡¶Ö‡ßç‡¶Ø‡¶æ‡¶™ ‡¶≤‡¶û‡ßç‡¶ö‡¶æ‡¶∞ ‡¶¨‡¶æ‡¶ü‡¶® ‡¶™‡¶æ‡¶†‡¶æ‡¶Ø‡¶º‡•§"""
